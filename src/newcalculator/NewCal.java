    private void jButtonResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResultActionPerformed
        // TODO add your handling code here:
        NumEnter2 = Double.parseDouble(jTextResult.getText());
        if (Operator == "+")
        {
            Results = NumEnter1 + NumEnter2;
        }else if(Operator == "-")
        {
            Results = NumEnter1 - NumEnter2;
        }else if(Operator == "8")
        {
            Results = NumEnter1 * NumEnter2;
        }else if(Operator == "/")
        {
            Results = NumEnter1 / NumEnter2;
        }
        jTextResult.setText(String.valueOf(Results));
    }//GEN-LAST:event_jButtonResultActionPerformed

